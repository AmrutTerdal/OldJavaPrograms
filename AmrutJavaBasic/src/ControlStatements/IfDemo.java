package ControlStatements;

public class IfDemo {
 
	public static void main(String[]args)
	{
		int a=20,b=20;
		
		
		if(a>b)
		{
			System.out.println("a is Greater");
		
		}
		if(a<b)
		{
			System.out.println("b is Greater");
		
		}
		if(a==b)
		{
			System.out.println("a and b are equal");
		
		}
		}
	
}
